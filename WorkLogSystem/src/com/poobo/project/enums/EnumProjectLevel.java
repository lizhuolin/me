package com.poobo.project.enums;

public enum EnumProjectLevel {
	 	LOW(0, "低"), NOR(1, "一般"), HIG(2, "高");
		
		private int value;
		private String desc;

		EnumProjectLevel(int value, String desc) {
			this.value = value;
		}

		public int getValue() {
			return value;
		}

		public String getDesc() {
			return desc;
		}
}
