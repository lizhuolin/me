package com.poobo.project.enums;
/**
 * 项目状态
 * @author zhuolin.li 2016年3月14日下午2:06:18
 *
 */
public enum EnumProjectStatus {
	 N_START(0, "未开始"), ING(1, "进行中"), OVER(2, "已结束");
	
	private int value;
	private String desc;

	EnumProjectStatus(int value, String desc) {
		this.value = value;
	}

	public int getValue() {
		return value;
	}

	public String getDesc() {
		return desc;
	}
}
